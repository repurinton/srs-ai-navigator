// Migrates the original National Service Line AI Navigator dataset into the
// SRS 2026 navigator, scrubbing organization-specific content for public use.
//
// Keeps: all use cases, curation, taxonomy, scoring inputs, investment tiers.
// Drops: `platformAlignment` (tied to a specific organization's platform stack)
//        and any originating-organization entries in deployment lists.
//
// Run:  node scripts/migrate-original.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const origData = join(here, "..", "..", "national-service-line-ai-navigator", "data");

// Original data files declare a top-level `const`; evaluate and capture it.
function loadVar(file, varName) {
  const code = readFileSync(join(origData, file), "utf8");
  // eslint-disable-next-line no-new-func
  return new Function(`${code}\n;return ${varName};`)();
}

const nslUseCases = loadVar("nsl-use-cases.js", "nslUseCases");
const investmentTierData = loadVar("investment-tiers.js", "investmentTierData");
const deployedAtData = loadVar("deployed-at.js", "deployedAtData");

// Normalize the three Heart/Lung/Vascular spellings and trim to canonical set.
function normalizeServiceLine(name) {
  const n = name.trim();
  if (/^heart/i.test(n)) return "Heart, Lung & Vascular";
  return n;
}

// Pattern for the originating organization's name, stripped from deployment lists.
const ORG_NAME = /advent\s*health/i;

let droppedPlatform = 0;
let strippedAdvent = 0;

const migrated = nslUseCases.map((uc) => {
  const { platformAlignment, ...rest } = uc;
  if (platformAlignment !== undefined) droppedPlatform++;

  const deployedAtRaw = deployedAtData[uc.id] ?? [];
  const deployedAt = deployedAtRaw.filter((s) => {
    const keep = !ORG_NAME.test(String(s));
    if (!keep) strippedAdvent++;
    return keep;
  });

  return {
    ...rest,
    serviceLines: (uc.serviceLines ?? []).map(normalizeServiceLine),
    investmentTier: investmentTierData[uc.id] ?? null,
    deployedAt,
    lens: "service-line",
  };
});

const banner =
  "// AUTO-GENERATED by scripts/migrate-original.mjs — do not edit by hand.\n" +
  "// De-branded migration of the original National Service Line AI Navigator dataset.\n\n";

const out =
  banner +
  "export const serviceLineUseCasesRaw: unknown[] = " +
  JSON.stringify(migrated, null, 2) +
  ";\n";

writeFileSync(join(here, "..", "src", "data", "service-line-use-cases.generated.ts"), out);

console.log(`Migrated ${migrated.length} use cases.`);
console.log(`Dropped platformAlignment from ${droppedPlatform} cases.`);
console.log(`Stripped ${strippedAdvent} originating-organization deployment entries.`);
console.log(
  `With investment tier: ${migrated.filter((m) => m.investmentTier).length}; ` +
    `with deployments: ${migrated.filter((m) => m.deployedAt.length).length}.`,
);
